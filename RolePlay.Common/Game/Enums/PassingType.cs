namespace RolePlay.Common.Game.Enums
{
    /// <summary>
    /// Object passability type
    /// </summary>
    public enum PassingType
    {
        /// <summary>
        /// An object that does not obstruct movement
        /// </summary>
        Passable = 0,
        /// <summary>
        /// An object that does not allow characters to move through itself
        /// </summary>
        Impassable = 1
    }
}
