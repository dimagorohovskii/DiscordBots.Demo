namespace RolePlay.Common.Game.Enums
{
    /// <summary>
    /// Type of cover provided by a battlefield object
    /// </summary>
    public enum CoverType
    {
        /// <summary>
        /// No cover is given to the character
        /// </summary>
        None = 0,
        /// <summary>
        /// The character is partially protected
        /// </summary>
        LightCover = 1,
        /// <summary>
        /// The character is fully protected
        /// </summary>
        FullCover = 2
    }
}
